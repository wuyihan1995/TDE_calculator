def clean_up():
    print("\n--- Clean Up ---\n")

    targets = []

    # Ed_calc generated files
    for pattern in ["logs", "dump_files"]:
        for p in glob.glob(os.path.join(ED_CALC_DIR, pattern)):
            if os.path.isdir(p):
                targets.append(p)

    for pattern in ["results", "results-*"]:
        for p in glob.glob(os.path.join(BASE_DIR, pattern)):
            if os.path.isdir(p):
                targets.append(p)

    for pattern in ["data.equilibrated.*", ".equilibrated_model.*",
                     "directions.config_*.txt",
                     "rss_substitute.lmp"]:
        for p in glob.glob(os.path.join(ED_CALC_DIR, pattern)):
            if os.path.isfile(p):
                targets.append(p)

    for pattern in ["Ed_summary.txt"]:
        for p in glob.glob(os.path.join(BASE_DIR, pattern)):
            if os.path.isfile(p):
                targets.append(p)

    # Ed_plot generated files
    for pattern in ["plot_Ed.png"]:
        for p in glob.glob(os.path.join(ED_PLOT_DIR, pattern)):
            if os.path.isfile(p):
                targets.append(p)

    if not targets:
        print("No intermediate or result files found. Nothing to clean.")
        return

    print("The following files/directories will be deleted:")
    for t in sorted(targets):
        rel = os.path.relpath(t, BASE_DIR)
        if os.path.isdir(t):
            size = sum(os.path.getsize(os.path.join(dp, fn))
                       for dp, _, fns in os.walk(t) for fn in fns)
            print(f"  [dir]  {rel}  ({size / 1024:.0f} KB)")
        else:
            print(f"  [file] {rel}")

    total_items = len(targets)
    confirm = input(f"\n  Delete {total_items} item(s)? [y/N]: ").strip().lower()
    if confirm != 'y':
        print("  Clean up cancelled.")
        return

    deleted = 0
    for t in targets:
        try:
            if os.path.isdir(t):
                shutil.rmtree(t)
            else:
                os.remove(t)
            deleted += 1
        except Exception as e:
            print(f"  ERROR deleting {os.path.relpath(t, BASE_DIR)}: {e}")

    print(f"  Deleted {deleted}/{total_items} items.")
    print("  Source code, potential files, and data files are preserved.")


