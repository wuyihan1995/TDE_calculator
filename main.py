def parse_run_ed_params():
    """Read key parameters from Ed_calc/run_Ed.sh."""
    params = {}
    run_ed = os.path.join(ED_CALC_DIR, "run_Ed.sh")
    if not os.path.exists(run_ed):
        return params
    with open(run_ed) as f:
        for line in f:
            m = re.match(r'^(\w+)=(\S+)', line.strip())
            if m and m.group(1) in (
                "CONFIG_SOURCE", "NCORE", "NJOB", "NCONFIG", "NDIR_PER_CONFIG",
                "SIMTIME", "EMIN", "EMAX", "ESTEP", "TEMP",
                "DEFECT_METHOD", "VERBOSE",
            ):
                val = m.group(2).strip('"').strip("'")
                params[m.group(1)] = val
    return params


def update_run_ed_param(name, value):
    """Update a single parameter in Ed_calc/run_Ed.sh using sed-style replacement."""
    run_ed = os.path.join(ED_CALC_DIR, "run_Ed.sh")
    with open(run_ed) as f:
        lines = f.readlines()
    pattern = re.compile(r'^(' + re.escape(name) + r'=)(\S+)')
    updated = False
    for i, line in enumerate(lines):
        m = pattern.match(line)
        if m:
            lines[i] = f"{m.group(1)}{value}\n"
            updated = True
            break
    if updated:
        with open(run_ed, 'w') as f:
            f.writelines(lines)
    return updated


def generate_alloy_conf(name, n_elements, elements, masses, lattice, potential,
                        config_source, crystal_structure="bcc",
                        lco_prefix="../custom_data_files/data"):
    """Write alloy.conf with the user-specified material parameters."""
    conf = os.path.join(ED_CALC_DIR, "alloy.conf")
    with open(conf, 'w') as f:
        f.write("#==============================================================================\n")
        f.write("# alloy.conf — Generated by main.py\n")
        f.write("#==============================================================================\n")
        f.write(f'\nALLOY_NAME="{name}"\n')
        f.write(f'N_ELEMENTS={n_elements}\n')
        f.write(f'LATTICE_CONSTANT={lattice}\n')
        f.write(f'ELEMENT_NAMES="{elements}"\n')
        f.write(f'ELEMENT_MASSES="{masses}"\n')
        f.write(f'\nPOTENTIAL_FILE="{potential}"\n')
        f.write(f'\nCONFIG_SOURCE="{config_source}"\n')
        f.write(f'\nCRYSTAL_STRUCTURE="{crystal_structure}"\n')
        if config_source == "custom":
            f.write(f'\nLCO_DATA_PREFIX="{lco_prefix}"\n')


def run_tde_calc():
    print("\n" + "=" * 60)
    print("  TDE Calculation — Material Setup")
    print("=" * 60)

    # ── Step 1: Elements ────────────────────────────────────────────────────
    print("\nStep 1: Enter element symbols (space-separated).")
    print("  Examples:  Hf Nb Zr Ti Ta   |   W Ta Cr V   |   Ta   |   Ma")
    default_elems = "Hf Nb Zr Ti Ta"
    elem_str = input(f"\n  Elements [{default_elems}]: ").strip()
    if not elem_str:
        elem_str = default_elems
    elems = elem_str.split()
    n_elements = len(elems)

    # Look up masses
    masses = []
    manual_mass = False
    for e in elems:
        if e in PERIODIC_TABLE:
            masses.append(f"{PERIODIC_TABLE[e]:.3f}")
        else:
            print(f"  '{e}' not in periodic table. Enter atomic mass manually.")
            m = input(f"    Mass of {e} (g/mol): ").strip()
            if not m:
                print("  ERROR: mass is required for unknown elements.")
                return
            masses.append(m)
            manual_mass = True
    masses_str = " ".join(masses)

    if manual_mass:
        print(f"  Masses: {masses_str} (some manually entered)")
    else:
        print(f"  Auto-looked up: {masses_str}")

    # ── Step 2: Potential ────────────────────────────────────────────────────
    print(f"\nStep 2: Select potential file from {os.path.relpath(POT_FILES_DIR, BASE_DIR)}/")
    pot_list = sorted(os.listdir(POT_FILES_DIR)) if os.path.exists(POT_FILES_DIR) else []
    if not pot_list:
        print(f"  No files found in {os.path.relpath(POT_FILES_DIR, BASE_DIR)}/.")
        print("  Please place a potential file in this directory first.")
        return
    for i, f in enumerate(pot_list):
        print(f"    {i+1}. {f}")
    try:
        choice = int(input(f"\n  Select [1-{len(pot_list)}]: ").strip())
        if choice < 1 or choice > len(pot_list):
            print("  Invalid selection.")
            return
    except (ValueError, EOFError):
        print("  Invalid input.")
        return
    pot_file = os.path.join("../pot_files", pot_list[choice - 1])
    print(f"  Selected: {pot_file}")

    # ── Step 3: Configuration source + lattice ───────────────────────────────
    print("\nStep 3: Configuration source.")
    print("  auto   = program generates random solid solution (BCC / FCC)")
    print("  custom = user provides data files in custom_data_files/")
    config_source = input("  Source [auto]: ").strip().lower()
    if not config_source:
        config_source = "auto"
    if config_source not in ("auto", "custom"):
        print("  Invalid choice. Must be 'auto' or 'custom'.")
        return

    crystal_structure = "bcc"
    if config_source == "auto":
        print("  Crystal structure (auto mode supports bcc and fcc only.")
        print("  For other structures, use 'custom' config source.)")
        crystal_structure = input("  Structure [bcc]: ").strip().lower()
        if not crystal_structure:
            crystal_structure = "bcc"
        if crystal_structure not in ("bcc", "fcc"):
            print("  Invalid choice. Must be 'bcc' or 'fcc'.")
            return

    lattice = input("  Lattice constant (Å) [3.40]: ").strip()
    if not lattice:
        lattice = "3.40"

    alloy_name = "-".join(elems)

    # ── Step 4: Simulation parameters ────────────────────────────────────────
    print("\nStep 4: Simulation parameters (press Enter to keep default).")
    params = parse_run_ed_params()
    if not params:
        print("  (using built-in defaults)")
        params = {"NCORE": "16", "NJOB": "4", "NCONFIG": "3",
                  "NDIR_PER_CONFIG": "100", "SIMTIME": "4.0",
                  "EMIN": "10", "EMAX": "150", "ESTEP": "2",
                  "TEMP": "40", "DEFECT_METHOD": "ovito"}

    editable = ["NCORE", "NJOB", "NCONFIG", "NDIR_PER_CONFIG",
                "SIMTIME", "EMIN", "EMAX", "ESTEP", "TEMP", "DEFECT_METHOD"]
    for key in editable:
        cur = params.get(key, "")
        hint = " [displace/cna/ptm/ovito]" if key == "DEFECT_METHOD" else ""
        new_val = input(f"  {key} [{cur}]{hint}: ").strip()
        if new_val:
            update_run_ed_param(key, new_val)
            params[key] = new_val

    # ── Step 5: Confirm ──────────────────────────────────────────────────────
    print("\n" + "=" * 60)
    print("  Summary")
    print("=" * 60)
    print(f"  Alloy:     {alloy_name}  ({elem_str})")
    print(f"  N elements: {n_elements}")
    print(f"  Potential: {pot_file}")
    print(f"  Config:    {config_source} ({params.get('NCONFIG','?')} configs)")
    if config_source == "auto":
        print(f"  Crystal:   {crystal_structure}")
    print(f"  Lattice:   {lattice} Å")
    print(f"  Cores:     {params.get('NCORE','?')} x {params.get('NJOB','?')} jobs")
    print(f"  Temp:      {params.get('TEMP','?')} K")
    print(f"  Defect:    {params.get('DEFECT_METHOD','?')}")

    confirm = input("\n  Proceed? [y/N]: ").strip().lower()
    if confirm != 'y':
        print("  Cancelled.")
        return

    # Generate alloy.conf and run
    generate_alloy_conf(alloy_name, n_elements, elem_str, masses_str,
                        lattice, pot_file, config_source, crystal_structure)

    print(f"\n  Alloy config written. Starting Ed calculation...")
    print(f"  Results will be saved to results/\n")

    try:
        subprocess.run(["bash", "run_Ed.sh"], cwd=ED_CALC_DIR)
    except KeyboardInterrupt:
        print("\n  TDE calculation interrupted.")
    except FileNotFoundError:
        print("ERROR: bash or run_Ed.sh not found. Check LAMMPS installation.")

    print("\n  TDE calculation finished.")


