def main():
    while True:
        show_menu()
        try:
            choice = input("  Select [0-4, 9]: ").strip()
        except EOFError:
            print("\n  Goodbye.\n")
            sys.exit(0)

        if choice == "0":
            print("\n  Goodbye.\n")
            sys.exit(0)
        elif choice == "1":
            run_tde_calc()
        elif choice == "2":
            run_ed_plot()
        elif choice == "3":
            run_dpa_calc()
        elif choice == "4":
            clean_up()
        elif choice == "9":
            run_env_setup()
        else:
            print("\n  Invalid choice. Please select 0-4 or 9.")

        if choice in ("1", "2", "3", "4", "9"):
            try:
                input("\n  Press Enter to return to menu...")
            except EOFError:
                break


if __name__ == "__main__":
    main()
