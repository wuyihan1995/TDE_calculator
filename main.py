def find_results_dirs():
    """Scan Ed_calc/ for directories containing config_*/Ed_direction_*.txt."""
    results = []
    pattern = os.path.join(BASE_DIR, "**", "config_*", "Ed_direction_*.txt")
    for fpath in glob.glob(pattern, recursive=True):
        d = os.path.dirname(os.path.dirname(fpath))  # .../results-xxx/
        if d not in results:
            results.append(d)
    return sorted(results)


def run_ed_plot():
    print("\n--- Plot Ed Convergence (Ed_plot) ---\n")

    results_dirs = find_results_dirs()
    if not results_dirs:
        print("No results found. Run TDE calculation first (option 1).")
        return

    print("Available results directories:")
    for i, d in enumerate(results_dirs):
        rel = os.path.relpath(d, BASE_DIR)
        n_files = len(glob.glob(os.path.join(d, "config_*", "Ed_direction_*.txt")))
        print(f"  {i+1}. {rel}  ({n_files} directions)")

    choice = input(f"\n  Select [1-{len(results_dirs)}]: ").strip()
    try:
        idx = int(choice) - 1
        if idx < 0 or idx >= len(results_dirs):
            print("Invalid selection.")
            return
    except ValueError:
        print("Invalid input.")
        return

    target_dir = results_dirs[idx]

    # Copy plot_Ed.py and alloy.conf if not present
    plot_script = os.path.join(target_dir, "plot_Ed.py")
    template = os.path.join(ED_PLOT_DIR, "plot_Ed.py")
    if not os.path.exists(plot_script) and os.path.exists(template):
        shutil.copy(template, plot_script)
        print(f"  Copied plot_Ed.py -> {os.path.relpath(target_dir, BASE_DIR)}/")

    alloy_conf_src = os.path.join(ED_CALC_DIR, "alloy.conf")
    alloy_conf_dst = os.path.join(target_dir, "alloy.conf")
    if os.path.exists(alloy_conf_src) and not os.path.exists(alloy_conf_dst):
        shutil.copy(alloy_conf_src, alloy_conf_dst)
        print(f"  Copied alloy.conf -> {os.path.relpath(target_dir, BASE_DIR)}/")

    if not os.path.exists(plot_script):
        print("ERROR: plot_Ed.py not found and could not be copied.")
        return

    print(f"\n  Plotting in {os.path.relpath(target_dir, BASE_DIR)}/ ...\n")
    try:
        subprocess.run(["python3", "plot_Ed.py"], cwd=target_dir)
    except KeyboardInterrupt:
        print("\n  Plotting interrupted.")

    png_path = os.path.join(target_dir, "plot_Ed.png")
    if os.path.exists(png_path):
        print(f"\n  Saved: {os.path.relpath(png_path, BASE_DIR)}")


