ENV_CONF_PATH = os.path.join(BASE_DIR, "env.conf")


def read_env_conf():
    """Read MPIRUN and LMP from env.conf. Returns dict with defaults."""
    defaults = {"MPIRUN": "mpirun", "LMP": "~/lmp-2023"}
    if not os.path.exists(ENV_CONF_PATH):
        return defaults
    with open(ENV_CONF_PATH) as f:
        for line in f:
            line = line.strip()
            if line.startswith("#") or not line:
                continue
            m = re.match(r'^(\w+)=(.*)$', line)
            if m and m.group(1) in defaults:
                defaults[m.group(1)] = m.group(2).strip().strip('"').strip("'")
    return defaults


def run_env_setup():
    print("\n--- Environment Setup ---\n")

    current = read_env_conf()
    print("Current settings:")
    print(f"  mpirun path: {current['MPIRUN']}")
    print(f"  LAMMPS path: {current['LMP']}")

    print("\nEnter new paths (press Enter to keep current):")
    new_mpirun = input(f"  mpirun path [{current['MPIRUN']}]: ").strip()
    new_lmp = input(f"  LAMMPS path [{current['LMP']}]: ").strip()

    mpirun_val = new_mpirun if new_mpirun else current["MPIRUN"]
    lmp_val = new_lmp if new_lmp else current["LMP"]

    with open(ENV_CONF_PATH, 'w') as f:
        f.write("# Environment configuration for TPE Calculator\n")
        f.write("# Set by main.py option 9. Sourced by Ed_calc/run_Ed.sh at startup.\n")
        f.write(f'MPIRUN="{mpirun_val}"\n')
        f.write(f'LMP="{lmp_val}"\n')

    print(f"\n  Saved to {os.path.relpath(ENV_CONF_PATH, BASE_DIR)}")
    print(f"  mpirun = {mpirun_val}")
    print(f"  LMP    = {lmp_val}")


